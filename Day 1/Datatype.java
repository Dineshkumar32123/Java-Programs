class Datatype
{
	public static void main(String args[])
	{
		byte b_value = 45;
		short s_value = 99;
		int i_value = 901;
		long l_value = 9944387074l;
		float f_value= 10.5f;
		double d_value= 123456886.58;

		System.out.println(b_value);
		System.out.println(s_value);
		System.out.println(i_value);
		System.out.println(l_value);
		System.out.println(f_value);
		System.out.println(d_value);



	}
}